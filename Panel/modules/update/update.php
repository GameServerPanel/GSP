function exec_ogp_module()
{
	if ($_SESSION['users_group'] !== 'admin') {
		print_failure(get_lang('no_access'));
		return;
	}

	require_once(dirname(__FILE__) . '/../administration/panel_update.php');
	gsp_panel_update_section();
